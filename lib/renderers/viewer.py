from __future__ import annotations

import json
from datetime import datetime
from pathlib import Path


def generate_viewer(
    output_dir: Path,
    items: list[dict],
    title: str = "Rafiki — Image Batch",
    run_meta: dict | None = None,
) -> Path:
    """Generate a single-run HTML viewer alongside the batch output images."""
    output_dir = Path(output_dir)
    meta = run_meta or {}

    timestamp = meta.get("timestamp", datetime.now().strftime("%Y-%m-%d %H:%M"))
    model = meta.get("model", "—")
    aspect_ratio = meta.get("aspect_ratio", "—")
    style = meta.get("style", "—")
    prompt_file = meta.get("prompt_file", "")

    js_items = []
    for item in items:
        out = Path(item.get("output_path", ""))
        try:
            rel = str(out.relative_to(output_dir))
        except ValueError:
            rel = out.name
        js_items.append({
            "name": item.get("name", "Untitled"),
            "prompt": item.get("prompt", ""),
            "file": rel,
            "ok": out.exists(),
        })

    html = _render_single(title, js_items, model, aspect_ratio, style, timestamp, prompt_file)
    viewer_path = output_dir / "viewer.html"
    viewer_path.write_text(html, encoding="utf-8")
    return viewer_path


def generate_comparison_viewer(project_dir: Path) -> Path:
    """Scan all run-* subdirs and generate a multi-run comparison viewer."""
    project_dir = Path(project_dir)

    run_json_paths = sorted(project_dir.glob("run-*/run.json"))
    runs = []
    for rjp in run_json_paths:
        try:
            data = json.loads(rjp.read_text(encoding="utf-8"))
            runs.append({
                "id": rjp.parent.name,
                "dir": rjp.parent.name,
                "timestamp": data.get("timestamp", ""),
                "model": data.get("model", ""),
                "style": data.get("style", "none"),
                "aspect_ratio": data.get("aspect_ratio", ""),
                "prompt_file": data.get("prompt_file", ""),
                "images": data.get("images", []),
            })
        except Exception:
            continue

    title = project_dir.name.replace("-", " ").replace("_", " ").title()
    html = _render_comparison(title, runs)
    viewer_path = project_dir / "viewer.html"
    viewer_path.write_text(html, encoding="utf-8")
    return viewer_path


# ─── Single-run renderer ─────────────────────────────────────────────────────

def _render_single(
    title: str,
    items: list[dict],
    model: str,
    aspect_ratio: str,
    style: str,
    timestamp: str,
    prompt_file: str,
) -> str:
    items_json = json.dumps(items, ensure_ascii=False)
    count = len(items)
    source_line = f"Source: <code>{prompt_file}</code> · " if prompt_file else ""

    return f"""<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width,initial-scale=1">
<title>{title}</title>
{_shared_css()}
</head>
<body>
<header>
  <h1>{title}</h1>
  <div class="meta">
    <span class="pill">{count} image{'' if count == 1 else 's'}</span>
    <span>Model: <code>{model}</code></span>
    <span>Ratio: <code>{aspect_ratio}</code></span>
    <span>Style: <code>{style}</code></span>
    <span>{source_line}Generated {timestamp}</span>
  </div>
</header>

<div class="grid" id="grid"></div>

{_lightbox_html()}

<footer>
  Generated by <code>rafiki</code> · {timestamp}
</footer>

<script>
const ITEMS = {items_json};
{_grid_js()}
{_lightbox_js()}
</script>
</body>
</html>"""


